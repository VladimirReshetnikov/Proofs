(**
  Finite Skolem hulls inside raw arithmetic structures.

  For a fixed syntax rank [selectorRank], [SkolemProgram] is the finite-tree
  language generated by a distinguished seed, the arithmetic operations, and
  one witness selector for every formula in [formula_rank_enum selectorRank].
  Its values form a raw arithmetic substructure.  The central theorem below
  proves rank-bounded elementarity of this substructure whenever the formula
  rank is at most the selector rank: existential witnesses and
  counterexamples to universal formulas are both among the selectors.

  This construction deliberately uses [RawPAModel], not [PA.Model]: the
  latter already contains a meta-level induction field and therefore cannot
  serve as a countermodel to an induction fragment.
*)

