Module PT := LeanProofsCoq.PowTower.LeanProofs.PowTower.

Definition IPowExpr := PT.Expr.

Definition atom : IPowExpr := PT.atom.
Definition pow : IPowExpr -> IPowExpr -> IPowExpr := PT.pow.

Inductive Sym : Type :=
| SI
| SNegI
| SQ
| SQInv
| SU
| SQQInv
| SV
| SW
| SQQ
| SQQInvBase
| SQQQ
| SQQQInv
| SSigma
| STau
| SITau
| SNearZero
| SNearOne
| SQV
| SQW
| SUnknown.

Definition q : Sym := SQ.

Definition powSym (base exponent : Sym) : Sym :=
  match base, exponent with
  | SI, SI => SQ
  | SI, SQ => SU
  | SI, SU => SV
  | SI, SNegI => SQInv
  | SI, STau => SITau
  | SITau, SI => SNearOne

  | SQ, SI => SNegI
  | SQ, SNegI => SI
  | SQ, SQ => SQQ
  | SQ, SQInv => SQQInvBase
  | SQ, SQQ => SQQQ
  | SQ, SQQInv => SQQQInv
  | SQ, SSigma => STau
  | SQ, STau => SNearOne
  | SQ, SV => SQV
  | SQ, SW => SQW

  | SNegI, SNegI => SQ
  | SNegI, SU => SW

  | SU, SQInv => SI
  | SU, SNegI => SQQInv

  | SQQ, SQInv => SQ
  | SQQ, SSigma => SNearZero

  | SQQInvBase, SQ => SQ
  | SQQQ, SQQInv => SQ
  | SQQQInv, SQQ => SQ

  | SQInv, SQInv => SSigma
  | STau, SQ => SNearZero

  | SQV, SW => SQ
  | SQW, SV => SQ

  | _, _ => SUnknown
  end.

Definition symbolicValue (e : IPowExpr) : Sym := PT.eval SI powSym e.

